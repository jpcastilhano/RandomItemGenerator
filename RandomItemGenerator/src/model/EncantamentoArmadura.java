package model;

public class EncantamentoArmadura {

	private TipoItens tipoEncantamento;
	private Integer melhoriaEncantamento;
	
	public TipoItens getTipoEncantamento() {
		return tipoEncantamento;
	}
	public void setTipoEncantamento(TipoItens tipoEncantamento) {
		this.tipoEncantamento = tipoEncantamento;
	}
	public Integer getMelhoriaEncantamento() {
		return melhoriaEncantamento;
	}
	public void setMelhoriaEncantamento(Integer melhoriaEncantamento) {
		this.melhoriaEncantamento = melhoriaEncantamento;
	}
}
